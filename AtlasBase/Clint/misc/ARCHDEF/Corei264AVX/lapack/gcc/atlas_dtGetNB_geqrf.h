#ifndef ATL_dtGetNB_geqrf

/*
 * NB selection for GEQRF: Side='RIGHT', Uplo='UPPER'
 * M : 25,120,160,200,240,480,1000,1040,1080,1120,1240,1360,1440,1480,1520,2040,2080,2120,2160,2200,2240,2280,2560,3080,4120
 * N : 25,120,160,200,240,480,1000,1040,1080,1120,1240,1360,1440,1480,1520,2040,2080,2120,2160,2200,2240,2280,2560,3080,4120
 * NB : 4,4,20,50,40,40,10,30,30,40,30,30,30,40,40,30,30,30,40,30,30,70,40,40,40
 */
#define ATL_dtGetNB_geqrf(n_, nb_) \
{ \
   if ((n_) < 140) (nb_) = 4; \
   else if ((n_) < 180) (nb_) = 20; \
   else if ((n_) < 220) (nb_) = 50; \
   else if ((n_) < 740) (nb_) = 40; \
   else if ((n_) < 1020) (nb_) = 10; \
   else if ((n_) < 1100) (nb_) = 30; \
   else if ((n_) < 1180) (nb_) = 40; \
   else if ((n_) < 1460) (nb_) = 30; \
   else if ((n_) < 1780) (nb_) = 40; \
   else if ((n_) < 2140) (nb_) = 30; \
   else if ((n_) < 2180) (nb_) = 40; \
   else if ((n_) < 2260) (nb_) = 30; \
   else if ((n_) < 2420) (nb_) = 70; \
   else (nb_) = 40; \
}


#endif    /* end ifndef ATL_dtGetNB_geqrf */
