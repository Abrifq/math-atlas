#ifndef ATL_cGetNB_geqrf

/*
 * NB selection for GEQRF: Side='RIGHT', Uplo='LOWER'
 * M : 25,50,75,100,125,150,175,200,250,300,350,400,450,500,600,700,800,900,1000,1200,1400,1600,1800,2000
 * N : 25,50,75,100,125,150,175,200,250,300,350,400,450,500,600,700,800,900,1000,1200,1400,1600,1800,2000
 * NB : 8,12,36,40,44,40,40,40,60,128,128,128,128,128,128,128,128,128,128,128,128,128,144,128
 */
#define ATL_cGetNB_geqrf(n_, nb_) \
{ \
   if ((n_) < 37) (nb_) = 8; \
   else if ((n_) < 62) (nb_) = 12; \
   else if ((n_) < 87) (nb_) = 36; \
   else if ((n_) < 112) (nb_) = 40; \
   else if ((n_) < 137) (nb_) = 44; \
   else if ((n_) < 225) (nb_) = 40; \
   else if ((n_) < 275) (nb_) = 60; \
   else if ((n_) < 1700) (nb_) = 128; \
   else if ((n_) < 1900) (nb_) = 144; \
   else (nb_) = 128; \
}


#endif    /* end ifndef ATL_cGetNB_geqrf */
